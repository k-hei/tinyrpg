WHITE = (0xFF, 0xFF, 0xFF)
BLACK = (0x00, 0x00, 0x00)
RED = (0xE6, 0x46, 0x46)
RED_DARK = (0x95, 0x1F, 0x45)
PURPLE = (0x8F, 0x3E, 0xCA)
PURPLE_DARK = (0x4B, 0x23, 0x8A)
YELLOW = (0xFB, 0xF2, 0x36)
YELLOW_DARK = (0xBA, 0x93, 0x35)
GREEN = (0x2E, 0xC1, 0x69)
GREEN_DARK = (0x25, 0x75, 0x5F)
GRAY = (0x7B, 0x74, 0x8A)
GRAY_DARK = (0x5A, 0x55, 0x5C)
BLUE = (0x3C, 0x59, 0xE3)
BLUE_DARK = (0x3D, 0x35, 0xAF)
CYAN = (0x00, 0xFF, 0xFF)
GOLD = (0xFC, 0xBC, 0x82)
GOLD_DARK = (0xE9, 0x9B, 0x54)

def darken(color):
  if color == WHITE: return GRAY
  elif color == RED: return RED_DARK
  elif color == PURPLE: return PURPLE_DARK
  elif color == YELLOW: return YELLOW_DARK
  elif color == GOLD: return GOLD_DARK
  elif color == GREEN: return GREEN_DARK
  elif color == GRAY: return GRAY_DARK
  elif color == BLUE: return BLUE_DARK
  return color
